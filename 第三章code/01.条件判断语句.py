##条件判断语句（if语句）
#语法：if 条件表达式 :语句
#执行流程：if语句执行时，会先对条件表达式进行求值判断，如果为True则执行if后的语句，如果为False则不执行
#默认情况下，if语句只会控制紧随其后的那条语句如果希望    if可以控制多条语句，则可以在if后跟着一个代码块
#代码块:代码块中保存着一组代码，同一个代码块中的代码，要么都执行要么都不执行
#代码块就是一种为代码分组的机制
#如果要编写代码块，语句就不能紧随在:后边，而是要写在下一行
#代码块要以缩进开始，直到代码恢复到之前的缩进级别时结束
#缩进有两种方式，一种是使用tab建，一种是使用空格（四个）
#Python的官方文档中推荐我们使用空格缩进 
